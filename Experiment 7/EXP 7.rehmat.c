#include<stdio.h>
int main()
{
    int n,i,a[100],large;
    printf("enter the number of elements:\n");
    scanf("%d",&n);
    for(i=0;i<=n-1;i++)
    {
        printf("enter a value:");
        scanf("%d",&a[i]);
    }
    large=a[0];
    for(i=1;i<=n-1;i++)
    {
        if(large<a[i])
            large=a[i];

    }
    printf("LARGEST NUMBER IS:%d",large);
 return 0;
}
